def apply_bot_name_to_lexicon(name: str) -> None:
    """Подставляет имя бота в шаблоны lexicon после get_me()."""
    for key, value in list(lexicon.items()):
        if isinstance(value, str) and "{bot_name}" in value:
            lexicon[key] = value.replace("{bot_name}", name)
        elif isinstance(value, tuple):
            lexicon[key] = tuple(
                item.replace("{bot_name}", name)
                if isinstance(item, str) and "{bot_name}" in item
                else item
                for item in value
            )
    global _PAYMENT_PRO_HEAD
    if "{bot_name}" in _PAYMENT_PRO_HEAD:
        _PAYMENT_PRO_HEAD = _PAYMENT_PRO_HEAD.replace("{bot_name}", name)


